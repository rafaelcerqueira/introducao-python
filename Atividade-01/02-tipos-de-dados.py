"""
Os principais tipos de dados são: inteiro, string (texto), flutuante (decimal), booleano, lista, tupla.

"""

#Exemplos de tipo de dado inteiro:
idade = 31
ano_de_nascimento = 1992

#Exemplos de tipo de dado texto:
mes_de_nascimento = "maio"
dia_do_nascimento = str(23)
dia_da_semana = "sábado"

#Exemplos de tipo de dado decimal:
media_da_disciplina = int(8.5)
preco_do_cafe = int(4.50)

#Exemplos de tipo de dado booleano:
gosta_de_cafe = True
gosta_de_cha = False

#Exemplos de tipo de dado lista:
lista_de_livros = ['Eu, Robô', '2001: Uma Odisseia No Espaço', 'Duna', 'Neuromancer']
lista_de_numeros = [1, 2, 3, 4, 5, 6, 7, 8, 9, 0]
lista_de_letras = ['q', 'w', 'e', 'r', 't']

#Exemplos de tipo de dado tupla:
anos_noventa = (1990, 1991, 1992, 1993, 1994, 1995, 1996, 1997, 1998, 1999)
vogais = ('a', 'e', 'i', 'o', 'u')

# Obs.: A diferença entre listas e tuplas é que, enquanto as listas podem ter seus valores alterados, as tuplas não permitem a alteração dos valores, somete a sobrescrição.